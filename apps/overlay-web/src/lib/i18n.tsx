function interpolate(template: string, params?: MessageParams): string {
  if (!params) {
    return template;
  }

  return template.replace(/\{\{(\w+)\}\}/g, (_match, key: string) => String(params[key] ?? ""));
}

function getInitialLanguage(): LanguageCode {
  if (typeof window === "undefined") {
    return "en";
  }

  const stored = window.localStorage.getItem(STORAGE_KEY);

  if (stored === "en" || stored === "zh") {
    return stored;
  }

  return window.navigator.language.toLowerCase().startsWith("zh") ? "zh" : "en";
}

function humanizeValue(value: string): string {
  return value
    .replace(/[-_]+/g, " ")
    .replace(/\b\w/g, (letter) => letter.toUpperCase());
}

interface I18nContextValue {
  language: LanguageCode;
  setLanguage: (language: LanguageCode) => void;
  t: (key: string, params?: MessageParams) => string;
  translateSignal: (value: string) => string;
  translateFreshness: (value: string) => string;
  translateCoverageState: (value: string) => string;
  translateStatus: (value: string) => string;
  translateEnvironment: (value: string) => string;
  translateProvider: (value: string) => string;
  translateRouteType: (value: string) => string;
  translateComponentType: (value: string) => string;
  translateTargetType: (value: string) => string;
  translateTargetSourceKind: (value: string) => string;
  translateEvidenceKind: (value: string) => string;
  translateEvidenceSource: (value: string) => string;
  translateEvidenceSubjectType: (value: string) => string;
  translateFindingType: (value: string) => string;
  translateRecommendationType: (value: string) => string;
  translateNodeType: (value: string) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

function buildEnumTranslator(t: (key: string, params?: MessageParams) => string, prefix: string) {
  return (value: string): string => {
    if (!value || value === "-") {
      return value;
    }

    const key = `${prefix}.${value}`;
    const translated = t(key);
    return translated === key ? humanizeValue(value) : translated;
  };
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<LanguageCode>(getInitialLanguage);

  useEffect(() => {
    if (typeof window !== "undefined") {
      window.localStorage.setItem(STORAGE_KEY, language);
      document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
    }
  }, [language]);

  const value = useMemo<I18nContextValue>(() => {
    const currentMessages = messages[language];
    const t = (key: string, params?: MessageParams) => interpolate(currentMessages[key] ?? key, params);

    return {
      language,
      setLanguage,
      t,
      translateSignal: buildEnumTranslator(t, "signal"),
      translateFreshness: buildEnumTranslator(t, "freshness"),
      translateCoverageState: buildEnumTranslator(t, "coverageState"),
      translateStatus: buildEnumTranslator(t, "status"),
      translateEnvironment: buildEnumTranslator(t, "environment"),
      translateProvider: buildEnumTranslator(t, "provider"),
      translateRouteType: buildEnumTranslator(t, "routeType"),
      translateComponentType: buildEnumTranslator(t, "componentType"),
      translateTargetType: buildEnumTranslator(t, "targetType"),
      translateTargetSourceKind: buildEnumTranslator(t, "sourceKind"),
      translateEvidenceKind: buildEnumTranslator(t, "evidenceKind"),
      translateEvidenceSource: buildEnumTranslator(t, "evidenceSource"),
      translateEvidenceSubjectType: buildEnumTranslator(t, "subjectType"),
      translateFindingType: buildEnumTranslator(t, "findingType"),
      translateRecommendationType: buildEnumTranslator(t, "recommendationType"),
      translateNodeType: buildEnumTranslator(t, "nodeType"),
    };
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const value = useContext(I18nContext);

  if (!value) {
    throw new Error("useI18n must be used within an I18nProvider.");
  }

  return value;
}
